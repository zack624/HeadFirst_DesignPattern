package edu.nust.behavioral.commandpattern;

public class CellingFan {
	public void high(){
		System.out.println("celling fan is highly running.");
	}
	
	public void medium(){
		System.out.println("celling fan is running in a medium speed.");
	}
	
	public void low(){
		System.out.println("celling fan is running in a low speed.");
	}
	
	public void off(){
		System.out.println("celling fan is off.");
	}
}
